# n (–Ω–µ –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ). –ò–º–µ–µ—Ç—Å—è —Å–ª–µ–¥—É—é—â–∏–π —Ä–µ–±—É—Å:
# üçå+3‚ãÖüíé+2‚ãÖü¶å=m
