# üçå + 3√óüíé + 2√óü¶å = m
# –≥–¥–µ üçå, üíé, ü¶å –∏ m ‚Äì —á–∏—Å–ª–∞, –ø—Ä–∏ –∫–æ—Ç–æ—Ä—ã—Ö —Ä–∞–≤–µ–Ω—Å—Ç–≤–æ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è.
#
# –ï—Å–ª–∏ –¥–ª—è –¥–∞–Ω–Ω–æ–≥–æ —Ä–µ–±—É—Å–∞ —Ä–µ—à–µ–Ω–∏–π –Ω–µ—Ç, –Ω—É–∂–Ω–æ –≤—ã–≤–µ—Å—Ç–∏ —Å–ª–µ–¥—É—é—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ:
# –ü—Ä–∏ –∑–∞–¥–∞–Ω–Ω—ã—Ö n –∏ m —Ä–µ—à–µ–Ω–∏–π –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç.
#
# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞ n –∏ m, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.
#
# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Ç–µ–∫—Å—Ç –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.
#
# –ü—Ä–∏–º–µ—Ä:
# –í—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ:
# 5
# 9
# –í—ã—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ:
# 1 + 3√ó2 + 2√ó1 = 9
# 2 + 3√ó1 + 2√ó2 = 9
# 4 + 3√ó1 + 2√ó1 = 9

n, m = int(input()), int(input())
flag = False
for i in range(1, n):
    for j in range(1, n):
        for k in range(1, n):
            if i + 3 * j + 2 * k == m:
                print(i, ' + 3√ó', j, ' + 2√ó', k, ' = ', m, sep='')
                flag = True
if not flag:
    print('–ü—Ä–∏ –∑–∞–¥–∞–Ω–Ω—ã—Ö n –∏ m —Ä–µ—à–µ–Ω–∏–π –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç')
